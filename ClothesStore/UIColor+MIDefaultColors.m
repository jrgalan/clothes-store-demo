#import "UIColor+MIDefaultColors.h"

@implementation UIColor (MIDefaultColors)

+ (UIColor *)defaultGreenColor {
    return [UIColor colorWithRed:0.f/255.f green:177.f/255.f blue:106.f/255.f alpha:1.f];
}

+ (UIColor *)defaultRedColor {
    return [UIColor colorWithRed:217.f/255.f green:30.f/255.f blue:24.f/255.f alpha:1.f];
}

+ (UIColor *)defaultLightGrayBackgroundColor {
    return [UIColor colorWithRed:246.f/255.f green:246.f/255.f blue:246.f/255.f alpha:1.f];
}

@end
